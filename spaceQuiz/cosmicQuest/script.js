// All Variables
let questionNumber = 0;
let trueAnswer = '';
let arrQuests = [];


// Functions _______________________________________________________
function randomizeQuestions(shuffle = []) {
    setQuestion.textContent = shuffle[0];
    let answers = [shuffle[1], shuffle[2], shuffle[3], shuffle[4], shuffle[5]];
    answers.sort(() => Math.random() - 0.5);
    setAns1.textContent = answers[0];
    setAns2.textContent = answers[1];
    setAns3.textContent = answers[2];
    setAns4.textContent = answers[3];
    setAns5.textContent = answers[4];
    trueAnswer = shuffle[1];
};

function checkAnswer(answer = '') {
    if (trueAnswer == answer) {
        document.getElementById(`part${questionNumber+1}`).classList.remove('hide');
    };

    if (questionNumber < 15) {
        questionNumber++;
        randomizeQuestions(arrQuests[questionNumber]);
    };     
};



